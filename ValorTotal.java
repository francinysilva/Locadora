public class ValorTotal {
	//classe com sobrecarga que soma valores
	public double vFinal (double a, double b) {
		return a+b;
	}
	public double vFinal (double a, double b, double c) {
		return a+b+c;
	}
	public double vFinal (double a, double b, double c, double d) {
		return a+b+c+d;
	}
	public double vFinal (double a, double b, double c, double d, double e) {
		return a+b+c+d+e;
	}
}
